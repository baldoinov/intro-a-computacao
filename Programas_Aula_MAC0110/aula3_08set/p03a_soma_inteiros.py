"""
    Programa 03a --  p03a_soma_inteiros.py
    ------------------------------------------- 
    Problema: 
    Dados dois números inteiros, determinar a sua soma.
"""
# --------------------------------------------------------
a = int(input("Digite o primeiro número inteiro: "))
b = int(input("Digite o segundo número inteiro: "))
    
soma = a + b
    
print(soma)

print(a, "+", b, "=", soma)

